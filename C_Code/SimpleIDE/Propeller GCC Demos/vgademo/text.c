void DrawHorzChar(int col, int row, uchar *ptr2, int stride2, int scale, int color)
{
    int i, j, k, l;
    int pixel, temp;

    for (j = 0; j < CHAR_HEIGHT; j++)
    {
	for (l = 0; l < scale; l++)
	{
            temp = *ptr2;
	    for (i = 0; i < CHAR_WIDTH; i++)
	    {
                pixel = temp & 128;
                temp <<= 1;
	        for (k = 0; k < scale; k++)
	        {
	            if(pixel)
		    {
                        drawdot(col, row, color);
		    }
                    col++;
	        }
	    }
            row++;
            col -= CHAR_WIDTH * scale;
	}
	ptr2 += stride2;
    }
}

void DrawVertChar(int col, int row, uchar *ptr2, int stride2, int scale, int color)
{
    int i, j, k, l;
    int pixel, temp;

    for (j = 0; j < CHAR_HEIGHT; j++)
    {
	for (l = 0; l < scale; l++)
	{
            temp = *ptr2;
	    for (i = 0; i < CHAR_WIDTH; i++)
	    {
                pixel = temp & 128;
                temp <<= 1;
	        for (k = 0; k < scale; k++)
	        {
	            if(pixel)
		    {
                        drawdot(col, row, color);
		    }
                    row--;
	        }
	    }
            col++;
            row += CHAR_WIDTH * scale;
	}
	ptr2 += stride2;
    }
}

